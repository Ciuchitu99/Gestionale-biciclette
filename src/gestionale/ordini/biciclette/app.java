    private String colorescelton(String color){
        String s = "";
        if(color.equals("Bianco")){
            return "01";
        }else if(color.equals("Grigio chiaro")){
            return "03";
        }else if(color.equals("Grigio scuro")){
            return "09";
        }else if(color.equals("Giallo")){
            return "14";
        }
        return s;
    }
    
